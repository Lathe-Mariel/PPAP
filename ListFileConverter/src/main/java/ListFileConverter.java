    void inputFileOpen(){
        JFileChooser filechooser;
        if(openFileDir != null){
            filechooser = new JFileChooser(new File(openFileDir));
        }else{
            filechooser = new JFileChooser();
        }

        int selected = filechooser.showOpenDialog(this);
        if (selected == JFileChooser.APPROVE_OPTION){
            File file = filechooser.getSelectedFile();
            openFileDir = file.getPath();

            inputTextArea.setText("");

            try{
                if (checkBeforeReadfile(file)){
                    BufferedReader br = new BufferedReader(new InputStreamReader(new FileInputStream(file), "UTF-8"));

                    String str;
                    while((str = br.readLine()) != null){
                        inputTextArea.append(str);
                        inputTextArea.append("\n");
                    }

                    br.close();
                }else{
                    System.out.println("ファイルが見つからないか開けません");
                }
            }catch(FileNotFoundException err){
                System.out.println(err);
            }catch(IOException err){
                System.out.println(err);
            }
        }
    }
    
    void outputFileSave(){
        JFileChooser filechooser;
        if(saveFileDir != null){
            filechooser = new JFileChooser(new File(saveFileDir));
        }else{
            filechooser = new JFileChooser();
        }
        filechooser.setDialogTitle("save as new file");

        int selected = filechooser.showSaveDialog(this);
        if (selected == JFileChooser.APPROVE_OPTION) {
            File file = filechooser.getSelectedFile();
            saveFileDir = file.getParent();
            try {
                FileOutputStream fos = new FileOutputStream(file.getPath() + ".txt");
                OutputStreamWriter osw = new OutputStreamWriter(fos, "UTF-8");
                String allString = outputTextArea.getText();

                osw.write(allString);
                osw.close();
                fos.close();
                messageTextField.setText("File is saved as " + file.getName());
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        } else if (selected == JFileChooser.CANCEL_OPTION) {
            messageTextField.setText("キャンセルされました");
        } else if (selected == JFileChooser.ERROR_OPTION) {
            messageTextField.setText("エラー又は取消しがありました");
        }        
    }
    
    private static boolean checkBeforeReadfile(File file){
        if (file.exists()){
            if (file.isFile() && file.canRead()){
                return true;
        }
    }
    return false;
  }
      
