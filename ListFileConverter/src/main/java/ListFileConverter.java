    public void addLineNumber(int columnNumber){
        if(columnNumber < 1 || columnNumber > 9)return;
        
        String allString = inputTextArea.getText();
        outputTextArea.setText("");
        String[] processText = allString.split("\n");
            for(int i=0; i < processText.length; i++){
                processText[i] = String.format("%" + columnNumber + "d：", i+1) + processText[i] + "\n";  //the number is should be started by 1, because that's line number.
                outputTextArea.append(processText[i]);            
        }
        messageTextField.setText("Line number is inserted");
    }
    
    public void convertWord(){
        String allString = outputTextArea.getText();
        outputTextArea.setText("");
        String[] processText = allString.split("\n");
        for(int i=0; i < processText.length; i++){
            processText[i].replaceAll(sourceTextField.getText(), targetTextField.getText());
            outputTextArea.append(processText[i] + "\n");
        }
    }
    
    public void convertTab2Space(){
        String allString = outputTextArea.getText();
        outputTextArea.setText("");
        String[] processText = allString.split("\n");
        for(int i=0; i < processText.length; i++){
            outputTextArea.append(processText[i].replaceAll("\t", "  ") + "\n");
        }    
    }

